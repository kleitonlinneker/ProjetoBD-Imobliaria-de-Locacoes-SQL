BANCO DE DADOS DE IMOBILIÁRIA QUE TRABALHA COM LOCAÇÕES DE IMÓVEIS

Uma Imobiliária Regional trabalha com locações de imóveis e deseja um banco de dados para gerenciar suas informações.
O banco de dados deve conter informações sobre funcionários (que podem ser corretores ou adm), inquilinos (que podem ser PF ou PJ),
imóveis, proprietários (que podem ser PF ou PJ), condomínios, pagamentos de aluguel, manutenções e prestadores de serviços.
