Corretor que realizou a locação, data de início, data de fim, valor e garantia, bem como as informações dos pagamentos de aluguel.
Um imóvel pode pertencer a nenhum ou a um condomínio e pode ter apenas um proprietário, em um condomínio podem ter um ou vários imóveis e um
proprietário pode possuir vários imóveis. Os imóveis podem precisar passar por nenhuma ou várias manutenções, mas uma manutenção pode ser feita
em apenas um imóvel, estas manutenções devem ser cuidadas por um funcionário adm, e os funcionários adm podem cuidar de várias manutenções.
Cada manutenção precisa ser realizada por um prestador de serviço, que por sua vez pode realizar várias manutenções.
